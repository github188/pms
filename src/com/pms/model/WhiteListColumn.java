package com.pms.model;

public class WhiteListColumn {
	private int id;
	private String columnKey;
	private String columnEng;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getColumnKey() {
		return columnKey;
	}
	public void setColumnKey(String columnKey) {
		this.columnKey = columnKey;
	}
	public String getColumnEng() {
		return columnEng;
	}
	public void setColumnEng(String columnEng) {
		this.columnEng = columnEng;
	}
}
